// 1) Array Destructuring :-

//[I] Basic example:
const numbers =[10,20,30];
const [a,b,c]=numbers;
// console.log(numbers);
console.log(a);
console.log(b);
console.log(c);

//[II] Skipping Elements:
const num=[50,60,70,80,90,100];
const [first, third] = num;
console.log(first);
console.log(third);





